x = "String"
print(x) # Will print "String"

x = 3
print(x) # Will print 3

x = True
print(x) # Will print True (aka a boolean)

x = 3.45
print(x) # Will print 3.45 (aka a float)

#Arithmetic
print(x + 5) # Addition, will add x and 5, print 8.45
print(x - 2) # Subtraction, will subtract 2 from x, print 1.45

#String concatenation
x = "Hello"
y = " World"
print(x + y) # Will print "Hello World"